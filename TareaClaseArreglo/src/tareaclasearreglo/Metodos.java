package tareaclasearreglo;

/**
 *
 * @author sigma
 */
public class Metodos {
    private int tamanio;
    private int indice;
    private int valor;

    public Metodos() {
    }

    public Metodos(int tamanio, int indice, int valor) {
        this.tamanio = tamanio;
        this.indice = indice;
        this.valor = valor;
    }

    public int getTamanio() {
        return tamanio;
    }

    public void setTamanio(int tamanio) {
        this.tamanio = tamanio;
    }

    public int getIndice() {
        return indice;
    }

    public void setIndice(int indice) {
        this.indice = indice;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
    
    
    
}
